import sys
sys.path.append('/Users/valeriaenriquezlimon/Documents/research-llm/llm-metaheuristics')
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(2)
prob = fun.get_formatted_problem()

heur = [
    ( # Search operator 1
    'gravitational_search',
    { 
        'gravity': 1.0,
        'alpha': 0.02
    },
    'all'
    ),
    (  
    'random_flight',
    {
        'scale': 1.0,
        'distribution': 'levy',
        'beta': 1.5
    },
    'probabilistic'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()
print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Short explanation and justification:
# This metaheuristic is named GravitationalSearchMetaheuristic as it combines the gravitational search algorithm with a random flight operator. The gravitational search uses parameters gravity and alpha to control the exploration vs exploitation trade-off, while the random flight utilizes scale, distribution (levy), and beta for exploring different regions of the search space probabilistically. Both operators are used in combination ('all' selector) to ensure comprehensive exploration and exploitation during the optimization process.
