import sys
sys.path.append('/Users/valeriaenriquezlimon/Documents/research-llm/llm-metaheuristics')
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(2)
prob = fun.get_formatted_problem()

heur = [
    ( # Gravitational Search Operator
        'gravitational_search',
        { 
            'gravity': 1.0,
            'alpha': 0.02
        },
        'metropolis'
    ),
    ( # Random Flight Operator
        'random_flight',
        { 
            'scale': 1.0,
            'distribution': 'levy',
            'beta': 1.5
        },
        'probabilistic'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()
print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Short explanation and justification:
# This metaheuristic combines the Gravitational Search Algorithm (with gravity parameter set to 1.0 and alpha to 0.02) with a Random Flight mechanism (scale set to 1.0, distribution as levy, and beta of 1.5). The Gravitational Search is selected for its global exploration capabilities, while the Random Flight operator introduces local search through probabilistic selection based on the distribution specified. This combination aims to leverage both broad exploration and targeted local search within a single framework, potentially improving convergence speed and quality of the solution.
