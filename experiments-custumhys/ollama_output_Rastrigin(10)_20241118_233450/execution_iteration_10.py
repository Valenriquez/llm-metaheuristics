# Name: Hybrid Metaheuristic with Spiral Dynamic and Swarm Dynamic Operators
# Code:
import sys
from pathlib import Path

project_dir = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_dir))
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(10)
prob = fun.get_formatted_problem()

heur = [
    (
        'spiral_dynamic',
        {
            'radius': 0.9,
            'angle': 22.5,
            'sigma': 0.1
        },
        'all'
    ),
    (
        'swarm_dynamic',
        {
            'factor': 0.7,
            'self_conf': 2.54,
            'swarm_conf': 2.56,
            'version': 'inertial',
            'distribution': 'uniform'
        },
        'all'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()

print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Initialise the fitness register
fitness = []
# Run the metaheuristic with the same problem 30 times
for rep in range(30):
    met = mh.Metaheuristic(prob, heur, num_iterations=1000, num_agents=2)
    met.reset_historicals()
    met.verbose = False
    met.run()
    print('rep = {}, x_best = {}, f_best = {}'.format(rep+1, *met.get_solution()))
    
    fitness.append(met.historical['fitness'])
    
# Short explanation and justification:
# This metaheuristic combines the Spiral Dynamic Operator with a modified version of the Swarm Dynamic Operator. The Spiral Dynamic Operator is designed to guide the search towards the optimal solution in a spiral manner, while the modified Swarm Dynamic Operator helps in maintaining exploration and diversity within the population. By using both operators together, we aim to balance exploitation and exploration effectively, leading to better convergence and robustness in solving optimization problems like Rastrigin's function.
# The use of 'all' as the selector ensures that all search agents are affected by both operators during each iteration, providing a more comprehensive search strategy. This hybrid approach should help in efficiently exploring the solution space and finding the global optimum for Rastrigin's function.