# Name: Hybrid Evolutionary Algorithm with Spiral Dynamic and Random Sample
# Code:
import sys
from pathlib import Path

project_dir = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_dir))
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(10) # This is the selected problem, the problem may vary depending on the case.
prob = fun.get_formatted_problem()

heur = [
    (  # Search operator 1
        'spiral_dynamic',
        {
            'radius': 0.9,
            'angle': 22.5,
            'sigma': 0.1
        },
        'all'
    ),
    (
        'random_sample',
        {},
        'greedy'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()

print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Initialise the fitness register
fitness = []
# Run the metaheuristic with the same problem 30 times
for rep in range(30):
    met = mh.Metaheuristic(prob, heur, num_iterations=1000, num_agents=2)
    met.reset_historicals()
    met.verbose = False
    met.run()
    print('rep = {}, x_best = {}, f_best = {}'.format(rep+1, *met.get_solution()))
    
    fitness.append(met.historical['fitness'])
    
# Short explanation and justification:
# This metaheuristic combines the Spiral Dynamic operator with a Random Sample operator. The Spiral Dynamic operator is used to explore the solution space in a more structured manner, while the Random Sample operator helps to escape local optima by providing random solutions. The use of 'all' selector for Spiral Dynamic ensures that it operates on all agents, and 'greedy' selector for Random Sample allows it to be utilized effectively during exploration phases.
# The hybrid approach aims to balance exploration and exploitation, leveraging the strengths of both operators to navigate through the Rastrigin function landscape efficiently.