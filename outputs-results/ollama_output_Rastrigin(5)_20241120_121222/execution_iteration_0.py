import sys
from pathlib import Path

project_dir = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_dir))
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(5) # Changed to Rastrigin function with dimension 5
prob = fun.get_formatted_problem()

heur = [
    (
        'spiral_dynamic',
        {
            'radius': 0.9,
            'angle': 22.5,
            'sigma': 0.1
        },
        'all'
    ),
    (
        'swarm_dynamic',
        {
            'factor': 0.7,
            'self_conf': 2.54,
            'swarm_conf': 2.56,
            'version': 'inertial',
            'distribution': 'uniform'
        },
        'all'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()

print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Initialise the fitness register
fitness = []
# Run the metaheuristic with the same problem 30 times
for rep in range(30):
    met = mh.Metaheuristic(prob, heur, num_iterations=1000, num_agents=2)
    met.reset_historicals()
    met.verbose = False
    met.run()
    print('rep = {}, x_best = {}, f_best = {}'.format(rep+1, *met.get_solution()))
    
    fitness.append(met.historical['fitness'])
    
# Short explanation and justification:
# This metaheuristic combines two popular stochastic search operators: Spiral Dynamic and Swarm Dynamic. 
# The Spiral Dynamic operator guides the agents to spiral towards the global optimum, which is effective for functions with multiple local minima. 
# The Swarm Dynamic operator helps in maintaining population diversity by updating the agents' positions based on their own best known position and the best known position of the entire swarm.
# Both operators are run concurrently using a 'all' selector, which ensures that each agent can use both strategies at every iteration. This hybrid approach is expected to leverage the strengths of both methods, leading to more robust optimization for the Rastrigin function.