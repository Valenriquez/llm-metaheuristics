# Name: Customized Metaheuristic for Rastrigin Function

# Code:
import sys
from pathlib import Path

project_dir = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_dir))
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(5)  # This is the selected problem with a dimension of 5.
prob = fun.get_formatted_problem()

heur = [
    (  # Search operator 1
        'spiral_dynamic',
        {
            'radius': 0.9,
            'angle': 22.5,
            'sigma': 0.1
        },
        'all'
    ),
    (
        'swarm_dynamic',
        {
            'factor': 0.7,
            'self_conf': 2.54,
            'swarm_conf': 2.56,
            'version': 'inertial',
            'distribution': 'gaussian'
        },
        'all'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()

print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Initialise the fitness register
fitness = []
# Run the metaheuristic with the same problem 30 times
for rep in range(30):
    met = mh.Metaheuristic(prob, heur, num_iterations=1000, num_agents=2)
    met.reset_historicals()
    met.verbose = False
    met.run()
    print('rep = {}, x_best = {}, f_best = {}'.format(rep+1, *met.get_solution()))
    
    fitness.append(met.historical['fitness'])
    
# Short explanation and justification:
# The chosen metaheuristic combines the Spiral Dynamic (spiral_dynamic) and Swarm Dynamic (swarm_dynamic) operators. These operators are well-suited for exploring and exploiting search spaces effectively. The Spiral Dynamic operator helps in searching around the best-known solution, while the Swarm Dynamic operator encourages a more diverse exploration of the solution space through the interaction between agents. The 'all' selector is used to apply both operators globally across the entire population. This combination aims to balance thorough exploration with efficient exploitation, making it suitable for the Rastrigin function, which has many local minima.