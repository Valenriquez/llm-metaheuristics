# Name: Hybrid Metaheuristic with Spiral Dynamic and Swarm Dynamics

# Code:
import sys
from pathlib import Path

project_dir = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_dir))
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(5)  # This is the selected problem.
prob = fun.get_formatted_problem()

# Initialize search operators with their parameters and selectors
heur = [
    (  # Search operator 1: Spiral Dynamic
        'spiral_dynamic',
        {
            'radius': 0.9,
            'angle': 22.5,
            'sigma': 0.1
        },
        'greedy'
    ),
    (
        'swarm_dynamic',
        {
            'factor': 0.7,
            'self_conf': 2.54,
            'swarm_conf': 2.56,
            'version': 'inertial',
            'distribution': 'uniform'
        },
        'all'
    )
]

# Create the metaheuristic
met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()

print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Initialize the fitness register
fitness = []
# Run the metaheuristic with the same problem 30 times
for rep in range(30):
    met = mh.Metaheuristic(prob, heur, num_iterations=1000, num_agents=2)
    met.reset_historicals()
    met.verbose = False
    met.run()
    print('rep = {}, x_best = {}, f_best = {}'.format(rep+1, *met.get_solution()))
    
    fitness.append(met.historical['fitness'])
    
# Short explanation and justification:
# This hybrid metaheuristic combines the Spiral Dynamic and Swarm Dynamics operators.
# Spiral Dynamic is good for exploration due to its spiral path through the search space.
# Swarm Dynamics helps in exploitation by mimicking social behavior and collective intelligence.
# The use of 'greedy' selector for Spiral Dynamic ensures quick convergence, while 'all' for Swarm Dynamics allows thorough exploration.
# This combination leverages the strengths of both operators to efficiently solve the Rastrigin function.