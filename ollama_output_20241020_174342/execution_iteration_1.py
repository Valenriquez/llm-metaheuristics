import sys
sys.path.append('/Users/valeriaenriquezlimon/Documents/research-llm/llm-metaheuristics')
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(2)
prob = fun.get_formatted_problem()

heur = [
    ( # Search operator 1
    'gravitational_search',
    { 
        'gravity': 1.0,
        'alpha': 0.02
    },
    'all'
    ),
    (  
    'random_flight',
    {
        'scale': 1.0,
        'distribution': 'levy',
        'beta': 1.5
    },
    'probabilistic'
    )
]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()
print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Short explanation and justification:
# This code defines a metaheuristic named GravitationalSearchMetaheuristic using the gravitational search algorithm with specified parameters for optimization. The gravitational search operator is configured with gravity and alpha values, while the random flight operator uses scale, distribution (levy), and beta values. Both operators are set to operate probabilistically over all possible solutions during the optimization process. This combination aims to leverage the global exploration capabilities of gravitational search and the local improvement facilitated by probabilistic selection in random flights. The benchmark function used is Rastrigin with dimension 2, ensuring a balance between simplicity and complexity for demonstration purposes.
