import sys
sys.path.append('/Users/valeriaenriquezlimon/Documents/research-llm/llm-metaheuristics')
import benchmark_func as bf
import metaheuristic as mh

fun = bf.Rastrigin(2)
prob = fun.get_formatted_problem()

heur = [( # Search operator 1
    'swarm_dynamic',
    {
        'factor': 0.7,
        'self_conf': 2.54,
        'swarm_conf': 2.56,
        'version': 'inertial',
        'distribution': 'gaussian'
    },
    'metropolis'
)]

met = mh.Metaheuristic(prob, heur, num_iterations=100)
met.verbose = True
met.run()
print('x_best = {}, f_best = {}'.format(*met.get_solution()))

# Short explanation and justification:
# This metaheuristic uses a custom search operator named 'swarm_dynamic' which includes parameters such as factor, self_conf, swarm_conf, version, and distribution. The selector used is 'metropolis', which is suitable for probabilistic selection in the context of optimization algorithms. The Rastrigin function is chosen as it is commonly used for testing global optimization problems due to its nonconvex nature, making it a good candidate for this metaheuristic approach.
